horas = int(input("Ingrese el las horas trabajadas: ")) #El "int" que esta antes del input transforma el texto que ingresemos nosotros en un "integer", es decir, un numero entero.
salario = int(input("Ingrese el salario por hora: "))
paga = horas * salario #El asterisco realiza una multiplicacion.
print("Su paga es de:", paga)