num = int(input("Ingrese un numero: "))
if ((num % 2) == 0): #Esto pregunto si el modulo (es decir el resto de una division) es igual a cero. Recordar que un solo "=" GUARDA informacion en una variable y dos "==" COMPARAN informacion entre datos.
    print("El numero ingresado es multiplo de 2")
else: 
    print("El numero ingresado no es multiplo de 2")



